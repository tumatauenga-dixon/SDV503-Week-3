//Arithmetic Operator

let x = 10;
let y = 5;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);

//1 Increment Operator
//Pre-Increment

let c = 5;

console.log(c);
console.log(++c);
console.log(c);
console.log(++c);
console.log(c);
console.log(++c);
console.log(c);

//Post-Increment

let b = 5;

console.log(b);
console.log(b++);
console.log(b);
console.log(b++);
console.log(b);
console.log(b++);
console.log(b);
console.log(b++);

//2 Decrement Operator
//Pre-decrement

console.log(c);
console.log(--c);
console.log(c);
console.log(--c);
console.log(c);
console.log(--c);
console.log(c);

//Post-decrement

console.log(b);
console.log(b--);
console.log(b);
console.log(b--);
console.log(b);
console.log(b--);
console.log(b);
console.log(b--);

